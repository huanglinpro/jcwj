public class Adm extends Base {
	
	private int admId;
	private String admName;
	private String admRealName;
	private String admPhone;
	private String admPress;
	private String admEmail;
	
	
	
	public String getEmail() {
		return admEmail;
	}
	public void setEmail(String email) {
		this.admEmail = email;
	}
	public int getAdmId() {
		return admId;
	}
	public void setAdmId(int admId) {
		this.admId = admId;
	}
	public String getAdmName() {
		return admName;
	}
	public void setAdmName(String admName) {
		this.admName = admName;
	}
	public String getAdmRealName() {
		return admRealName;
	}
	public void setAdmRealName(String admRealName) {
		this.admRealName = admRealName;
	}
	public String getAdmPhone() {
		return admPhone;
	}
	public void setAdmPhone(String admPhone) {
		this.admPhone = admPhone;
	}
	public String getAdmPress() {
		return admPress;
	}
	public void setAdmPress(String admPress) {
		this.admPress = admPress;
	}
	public String getAdmEmail() {
		return admEmail;
	}
	public void setAdmEmail(String admEmail) {
		this.admEmail = admEmail;
	}
	public Adm() {
		super();
	}
	
	
	
	
	
	

}
